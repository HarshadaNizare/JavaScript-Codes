document.write("JavaScript Program to check for Even or Odd Number");
let no=parseInt(prompt("Enter any number to check for Even or Odd:-"));
console.log("Number="+no);

if(no%2==0)
{
  document.write("<br>Number is EVEN");
  console.log("Number is EVEN");
}
else
{
  document.write("<br>Number is ODD");
  console.log("Number is ODD");
}
